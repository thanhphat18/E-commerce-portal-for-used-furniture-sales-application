    private boolean isNumeric(String s){
        try{
            double d = Double.parseDouble(s);
            return true;
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, ""+ex + "\nNumeric value is required!(price field)");
        }
        return false;
    }
    
